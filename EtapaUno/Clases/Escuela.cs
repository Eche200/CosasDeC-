class Escuela
{


    public string nombre;
    public int anoDeCreacion;
    public string paisDeLaEscuela;


    public Escuela(){

    }


    public string Nombre
    {
        get
        {
            return this.nombre;
        }
        set
        {
            this.nombre = value;
        }
    }
    public int AnoDeCreacion
    {
        get
        {
            return this.anoDeCreacion;
        }
        set
        {
            this.anoDeCreacion = value;
        }
    }
    public string PaisDeLaEscuela
    {
        get
        {
            return this.paisDeLaEscuela;
        }
        set
        {
            this.paisDeLaEscuela = value;
        }
    }











}